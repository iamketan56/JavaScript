//reverse function will reverse the array
let letters = ['K', 'E', 'T', 'A', 'N'];
console.log(letters);
console.log(letters.reverse());

//join method help to join anything with array element
console.log(letters.join('-'));//N-A-T-E-K

console.log(letters.join('&'));//N&A&T&E&K

console.log(letters.join(' % '));//N % A % T % E % K

